import { type Type } from '~/types';

export function typeAdvantageLookup(selfTypes: Type[], enemyType: Type) {
  return selfTypes
    .map((selfType) => {
      const enemyTypeIndex = typeIndexMap[enemyType];
      const selfTypeIndex = typeIndexMap[selfType];
      return strengthChart[enemyTypeIndex][selfTypeIndex];
    })
    .reduce((a, b) => a * b, 1);
}

